-- 5) RELACIONAMENTO N:N FOCO x MEDICAO (associativa)
-- ---------------------------------------------------------------------

-- 5.1) T_FOCO_CONDICAO_CLIM -------------------------------------------
CREATE TABLE T_FOCO_CONDICAO_CLIM (
    ID_FOCO        NUMBER(12)   NOT NULL,
    ID_MEDICAO     NUMBER(12)   NOT NULL,
    DISTANCIA_KM   NUMBER(8,3),
    DIF_TEMPO_MIN  NUMBER(8),
    CONSTRAINT PK_FOCO_COND      PRIMARY KEY (ID_FOCO, ID_MEDICAO),
    CONSTRAINT FK_COND_FOCO      FOREIGN KEY (ID_FOCO)
        REFERENCES T_FOCO_CALOR (ID_FOCO),
    CONSTRAINT FK_COND_MEDICAO   FOREIGN KEY (ID_MEDICAO)
        REFERENCES T_MEDICAO_CLIMATICA (ID_MEDICAO),
    CONSTRAINT CK_COND_DIST      CHECK (DISTANCIA_KM >= 0)
);

COMMENT ON TABLE  T_FOCO_CONDICAO_CLIM               IS 'Associativa N:N entre foco de calor e medicao climatica mais proxima (espaco-tempo).';
COMMENT ON COLUMN T_FOCO_CONDICAO_CLIM.ID_FOCO       IS 'FK para T_FOCO_CALOR (parte da PK composta).';
COMMENT ON COLUMN T_FOCO_CONDICAO_CLIM.ID_MEDICAO    IS 'FK para T_MEDICAO_CLIMATICA (parte da PK composta).';
COMMENT ON COLUMN T_FOCO_CONDICAO_CLIM.DISTANCIA_KM  IS 'Distancia (km) entre o foco e a estacao da medicao associada.';
COMMENT ON COLUMN T_FOCO_CONDICAO_CLIM.DIF_TEMPO_MIN IS 'Diferenca de tempo (minutos) entre a passagem do foco e a medicao.';

