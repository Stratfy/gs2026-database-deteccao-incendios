-- 4.1) T_ESTACAO_METEOROLOGICA ----------------------------------------
CREATE TABLE T_ESTACAO_METEOROLOGICA (
    ID_ESTACAO    NUMBER(6)      NOT NULL,
    CD_WMO        VARCHAR2(10)   NOT NULL,
    NM_ESTACAO    VARCHAR2(120)  NOT NULL,
    ID_ESTADO     NUMBER(4)      NOT NULL,
    DT_FUNDACAO   DATE,
    CONSTRAINT PK_ESTACAO          PRIMARY KEY (ID_ESTACAO),
    CONSTRAINT FK_ESTACAO_ESTADO   FOREIGN KEY (ID_ESTADO)
        REFERENCES T_ESTADO (ID_ESTADO),
    CONSTRAINT UK_ESTACAO_WMO      UNIQUE (CD_WMO)
);

COMMENT ON TABLE  T_ESTACAO_METEOROLOGICA              IS 'Estacoes meteorologicas automaticas do INMET (BDMEP).';
COMMENT ON COLUMN T_ESTACAO_METEOROLOGICA.ID_ESTACAO  IS 'Identificador substituto da estacao (PK).';
COMMENT ON COLUMN T_ESTACAO_METEOROLOGICA.CD_WMO      IS 'Codigo WMO/OMM da estacao (unico). Ex.: A001.';
COMMENT ON COLUMN T_ESTACAO_METEOROLOGICA.NM_ESTACAO  IS 'Nome da estacao meteorologica.';
COMMENT ON COLUMN T_ESTACAO_METEOROLOGICA.ID_ESTADO   IS 'FK para T_ESTADO: UF da estacao.';
COMMENT ON COLUMN T_ESTACAO_METEOROLOGICA.DT_FUNDACAO IS 'Data de fundacao/inicio de operacao da estacao.';

-- 4.2) T_LOCALIZACAO_ESTACAO (relacao 1:1 com a estacao) --------------
CREATE TABLE T_LOCALIZACAO_ESTACAO (
    ID_ESTACAO    NUMBER(6)      NOT NULL,
    LATITUDE      NUMBER(9,6)    NOT NULL,
    LONGITUDE     NUMBER(9,6)    NOT NULL,
    ALTITUDE_M    NUMBER(7,2),
    CONSTRAINT PK_LOC_ESTACAO     PRIMARY KEY (ID_ESTACAO),
    CONSTRAINT FK_LOC_ESTACAO     FOREIGN KEY (ID_ESTACAO)
        REFERENCES T_ESTACAO_METEOROLOGICA (ID_ESTACAO),
    CONSTRAINT CK_LOC_LAT         CHECK (LATITUDE  BETWEEN -90  AND 90),
    CONSTRAINT CK_LOC_LON         CHECK (LONGITUDE BETWEEN -180 AND 180)
);

COMMENT ON TABLE  T_LOCALIZACAO_ESTACAO            IS 'Geolocalizacao da estacao (1:1 com T_ESTACAO_METEOROLOGICA). Decompoe atributos espaciais.';
COMMENT ON COLUMN T_LOCALIZACAO_ESTACAO.ID_ESTACAO IS 'PK e FK 1:1 para T_ESTACAO_METEOROLOGICA.';
COMMENT ON COLUMN T_LOCALIZACAO_ESTACAO.LATITUDE   IS 'Latitude da estacao (graus decimais).';
COMMENT ON COLUMN T_LOCALIZACAO_ESTACAO.LONGITUDE  IS 'Longitude da estacao (graus decimais).';
COMMENT ON COLUMN T_LOCALIZACAO_ESTACAO.ALTITUDE_M IS 'Altitude da estacao (metros).';

-- 4.3) T_MEDICAO_CLIMATICA --------------------------------------------
CREATE TABLE T_MEDICAO_CLIMATICA (
    ID_MEDICAO       NUMBER(12)    NOT NULL,
    ID_ESTACAO       NUMBER(6)     NOT NULL,
    DT_HORA_UTC      TIMESTAMP     NOT NULL,
    PRECIPITACAO_MM  NUMBER(7,2),
    PRESSAO_HPA      NUMBER(7,2),
    PRESSAO_MAX_HPA  NUMBER(7,2),
    PRESSAO_MIN_HPA  NUMBER(7,2),
    RADIACAO_KJ_M2   NUMBER(9,2),
    TEMP_AR_C        NUMBER(5,2),
    TEMP_ORVALHO_C   NUMBER(5,2),
    TEMP_MAX_C       NUMBER(5,2),
    TEMP_MIN_C       NUMBER(5,2),
    UMIDADE_REL_PCT  NUMBER(5,2),
    UMIDADE_MAX_PCT  NUMBER(5,2),
    UMIDADE_MIN_PCT  NUMBER(5,2),
    VENTO_DIR_GRAUS  NUMBER(5,2),
    VENTO_RAJADA_MS  NUMBER(6,2),
    VENTO_VEL_MS     NUMBER(6,2),
    CONSTRAINT PK_MEDICAO           PRIMARY KEY (ID_MEDICAO),
    CONSTRAINT FK_MEDICAO_ESTACAO   FOREIGN KEY (ID_ESTACAO)
        REFERENCES T_ESTACAO_METEOROLOGICA (ID_ESTACAO),
    CONSTRAINT UK_MEDICAO_EST_DT    UNIQUE (ID_ESTACAO, DT_HORA_UTC),
    CONSTRAINT CK_MED_UMID_REL      CHECK (UMIDADE_REL_PCT BETWEEN 0 AND 100),
    CONSTRAINT CK_MED_UMID_MAX      CHECK (UMIDADE_MAX_PCT BETWEEN 0 AND 100),
    CONSTRAINT CK_MED_UMID_MIN      CHECK (UMIDADE_MIN_PCT BETWEEN 0 AND 100),
    CONSTRAINT CK_MED_VENTO_DIR     CHECK (VENTO_DIR_GRAUS BETWEEN 0 AND 360),
    CONSTRAINT CK_MED_PRECIP        CHECK (PRECIPITACAO_MM >= 0),
    CONSTRAINT CK_MED_VENTO_VEL     CHECK (VENTO_VEL_MS >= 0),
    CONSTRAINT CK_MED_VENTO_RAJ     CHECK (VENTO_RAJADA_MS >= 0)
);

COMMENT ON TABLE  T_MEDICAO_CLIMATICA                 IS 'Medicoes horarias das estacoes do INMET (variaveis meteorologicas).';
COMMENT ON COLUMN T_MEDICAO_CLIMATICA.ID_MEDICAO      IS 'Identificador substituto da medicao (PK).';
COMMENT ON COLUMN T_MEDICAO_CLIMATICA.ID_ESTACAO      IS 'FK para T_ESTACAO_METEOROLOGICA.';
COMMENT ON COLUMN T_MEDICAO_CLIMATICA.DT_HORA_UTC     IS 'Data/hora UTC da medicao horaria.';
COMMENT ON COLUMN T_MEDICAO_CLIMATICA.PRECIPITACAO_MM IS 'Precipitacao na hora (mm).';
COMMENT ON COLUMN T_MEDICAO_CLIMATICA.PRESSAO_HPA     IS 'Pressao atmosferica ao nivel da estacao (hPa).';
COMMENT ON COLUMN T_MEDICAO_CLIMATICA.RADIACAO_KJ_M2  IS 'Radiacao global (kJ/m2).';
COMMENT ON COLUMN T_MEDICAO_CLIMATICA.TEMP_AR_C       IS 'Temperatura do ar - bulbo seco (C).';
COMMENT ON COLUMN T_MEDICAO_CLIMATICA.TEMP_ORVALHO_C  IS 'Temperatura do ponto de orvalho (C).';
COMMENT ON COLUMN T_MEDICAO_CLIMATICA.UMIDADE_REL_PCT IS 'Umidade relativa do ar (%). Variavel-chave para risco de fogo.';
COMMENT ON COLUMN T_MEDICAO_CLIMATICA.VENTO_DIR_GRAUS IS 'Direcao do vento (graus, 0..360).';
COMMENT ON COLUMN T_MEDICAO_CLIMATICA.VENTO_RAJADA_MS IS 'Rajada maxima de vento (m/s).';
COMMENT ON COLUMN T_MEDICAO_CLIMATICA.VENTO_VEL_MS    IS 'Velocidade media do vento (m/s).';

