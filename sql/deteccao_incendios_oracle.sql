-- ---------------------------------------------------------------------
-- 2) TABELAS DE DOMINIO / DIMENSAO
-- ---------------------------------------------------------------------

-- 2.1) T_BIOMA --------------------------------------------------------
CREATE TABLE T_BIOMA (
    ID_BIOMA   NUMBER(4)      NOT NULL,
    NM_BIOMA   VARCHAR2(40)   NOT NULL,
    CONSTRAINT PK_BIOMA       PRIMARY KEY (ID_BIOMA),
    CONSTRAINT UK_BIOMA_NOME  UNIQUE (NM_BIOMA)
);

COMMENT ON TABLE  T_BIOMA            IS 'Biomas brasileiros monitorados (classificacao IBGE/MapBiomas).';
COMMENT ON COLUMN T_BIOMA.ID_BIOMA   IS 'Identificador substituto do bioma (PK).';
COMMENT ON COLUMN T_BIOMA.NM_BIOMA   IS 'Nome do bioma: Amazonia, Cerrado, Caatinga, Mata Atlantica, Pantanal, Pampa.';

-- 2.2) T_ESTADO -------------------------------------------------------
CREATE TABLE T_ESTADO (
    ID_ESTADO   NUMBER(4)     NOT NULL,
    NM_ESTADO   VARCHAR2(60)  NOT NULL,
    SG_UF       CHAR(2)       NOT NULL,
    NM_REGIAO   VARCHAR2(15)  NOT NULL,
    CONSTRAINT PK_ESTADO       PRIMARY KEY (ID_ESTADO),
    CONSTRAINT UK_ESTADO_UF    UNIQUE (SG_UF),
    CONSTRAINT CK_ESTADO_REGIAO CHECK
        (NM_REGIAO IN ('Norte','Nordeste','Centro-Oeste','Sudeste','Sul'))
);

COMMENT ON TABLE  T_ESTADO            IS 'Unidades da Federacao (27 UFs), codigos IBGE.';
COMMENT ON COLUMN T_ESTADO.ID_ESTADO  IS 'Codigo IBGE da UF (PK). Ex.: 35 = Sao Paulo.';
COMMENT ON COLUMN T_ESTADO.NM_ESTADO  IS 'Nome da Unidade da Federacao.';
COMMENT ON COLUMN T_ESTADO.SG_UF      IS 'Sigla da UF (2 letras). Unica.';
COMMENT ON COLUMN T_ESTADO.NM_REGIAO  IS 'Regiao geografica (Norte/Nordeste/Centro-Oeste/Sudeste/Sul).';

-- 2.3) T_MUNICIPIO ----------------------------------------------------
CREATE TABLE T_MUNICIPIO (
    ID_MUNICIPIO    NUMBER(8)      NOT NULL,
    NM_MUNICIPIO    VARCHAR2(120)  NOT NULL,
    ID_ESTADO       NUMBER(4)      NOT NULL,
    CD_MICRORREGIAO NUMBER(6),
    LATITUDE        NUMBER(9,6),
    LONGITUDE       NUMBER(9,6),
    CONSTRAINT PK_MUNICIPIO       PRIMARY KEY (ID_MUNICIPIO),
    CONSTRAINT FK_MUNICIPIO_ESTADO FOREIGN KEY (ID_ESTADO)
        REFERENCES T_ESTADO (ID_ESTADO),
    CONSTRAINT UK_MUNICIPIO_NOME_UF UNIQUE (NM_MUNICIPIO, ID_ESTADO),
    CONSTRAINT CK_MUNICIPIO_LAT   CHECK (LATITUDE  BETWEEN -90  AND 90),
    CONSTRAINT CK_MUNICIPIO_LON   CHECK (LONGITUDE BETWEEN -180 AND 180)
);

COMMENT ON TABLE  T_MUNICIPIO                 IS 'Municipios brasileiros (codigo IBGE 7 digitos) com centroide.';
COMMENT ON COLUMN T_MUNICIPIO.ID_MUNICIPIO    IS 'Codigo IBGE do municipio (7 digitos) - PK.';
COMMENT ON COLUMN T_MUNICIPIO.NM_MUNICIPIO    IS 'Nome do municipio.';
COMMENT ON COLUMN T_MUNICIPIO.ID_ESTADO       IS 'FK para T_ESTADO (UF do municipio).';
COMMENT ON COLUMN T_MUNICIPIO.CD_MICRORREGIAO IS 'Codigo IBGE da microrregiao.';
COMMENT ON COLUMN T_MUNICIPIO.LATITUDE        IS 'Latitude do centroide do municipio (graus decimais).';
COMMENT ON COLUMN T_MUNICIPIO.LONGITUDE       IS 'Longitude do centroide do municipio (graus decimais).';

-- 2.4) T_SATELITE -----------------------------------------------------
CREATE TABLE T_SATELITE (
    ID_SATELITE    NUMBER(4)     NOT NULL,
    NM_SATELITE    VARCHAR2(40)  NOT NULL,
    FL_REFERENCIA  CHAR(1)       DEFAULT 'N' NOT NULL,
    CONSTRAINT PK_SATELITE        PRIMARY KEY (ID_SATELITE),
    CONSTRAINT UK_SATELITE_NOME   UNIQUE (NM_SATELITE),
    CONSTRAINT CK_SATELITE_REF    CHECK (FL_REFERENCIA IN ('S','N'))
);

COMMENT ON TABLE  T_SATELITE               IS 'Satelites de monitoramento de focos de calor do programa BDQueimadas/INPE.';
COMMENT ON COLUMN T_SATELITE.ID_SATELITE   IS 'Identificador substituto do satelite (PK).';
COMMENT ON COLUMN T_SATELITE.NM_SATELITE   IS 'Nome do satelite/sensor. Ex.: AQUA_M-T, NOAA-20, GOES-19.';
COMMENT ON COLUMN T_SATELITE.FL_REFERENCIA IS 'Flag S/N: satelite de referencia do INPE para comparacao historica (AQUA_M-T = S).';

-- ---------------------------------------------------------------------
-- 3) TABELA FATO PRINCIPAL: FOCOS DE CALOR
-- ---------------------------------------------------------------------

-- 3.1) T_FOCO_CALOR ---------------------------------------------------
CREATE TABLE T_FOCO_CALOR (
    ID_FOCO            NUMBER(12)    NOT NULL,
    FOCO_ID_BDQ        VARCHAR2(40),
    ID_SATELITE        NUMBER(4)     NOT NULL,
    ID_MUNICIPIO       NUMBER(8),
    ID_BIOMA           NUMBER(4),
    DT_PASSAGEM        TIMESTAMP     NOT NULL,
    LATITUDE           NUMBER(9,6)   NOT NULL,
    LONGITUDE          NUMBER(9,6)   NOT NULL,
    NUM_DIAS_SEM_CHUVA NUMBER(4),
    PRECIPITACAO_MM    NUMBER(7,2),
    RISCO_FOGO         NUMBER(4,3),
    FRP_MW             NUMBER(9,2),
    CONSTRAINT PK_FOCO_CALOR        PRIMARY KEY (ID_FOCO),
    CONSTRAINT FK_FOCO_SATELITE     FOREIGN KEY (ID_SATELITE)
        REFERENCES T_SATELITE (ID_SATELITE),
    CONSTRAINT FK_FOCO_MUNICIPIO    FOREIGN KEY (ID_MUNICIPIO)
        REFERENCES T_MUNICIPIO (ID_MUNICIPIO),
    CONSTRAINT FK_FOCO_BIOMA        FOREIGN KEY (ID_BIOMA)
        REFERENCES T_BIOMA (ID_BIOMA),
    CONSTRAINT CK_FOCO_LAT          CHECK (LATITUDE  BETWEEN -90  AND 90),
    CONSTRAINT CK_FOCO_LON          CHECK (LONGITUDE BETWEEN -180 AND 180),
    CONSTRAINT CK_FOCO_RISCO        CHECK (RISCO_FOGO BETWEEN 0 AND 1),
    CONSTRAINT CK_FOCO_DIAS         CHECK (NUM_DIAS_SEM_CHUVA >= 0),
    CONSTRAINT CK_FOCO_PRECIP       CHECK (PRECIPITACAO_MM >= 0),
    CONSTRAINT CK_FOCO_FRP          CHECK (FRP_MW >= 0)
);

COMMENT ON TABLE  T_FOCO_CALOR                    IS 'Tabela fato: focos de calor detectados por satelite (BDQueimadas/INPE, 2025).';
COMMENT ON COLUMN T_FOCO_CALOR.ID_FOCO            IS 'Identificador substituto do foco (PK).';
COMMENT ON COLUMN T_FOCO_CALOR.FOCO_ID_BDQ        IS 'Identificador de origem no BDQueimadas (quando disponivel).';
COMMENT ON COLUMN T_FOCO_CALOR.ID_SATELITE        IS 'FK para T_SATELITE: satelite que detectou o foco.';
COMMENT ON COLUMN T_FOCO_CALOR.ID_MUNICIPIO       IS 'FK para T_MUNICIPIO: municipio do foco (pode ser nulo se nao casado com IBGE).';
COMMENT ON COLUMN T_FOCO_CALOR.ID_BIOMA           IS 'FK para T_BIOMA: bioma do foco.';
COMMENT ON COLUMN T_FOCO_CALOR.DT_PASSAGEM        IS 'Data/hora UTC da passagem do satelite que registrou o foco.';
COMMENT ON COLUMN T_FOCO_CALOR.LATITUDE           IS 'Latitude do foco (graus decimais).';
COMMENT ON COLUMN T_FOCO_CALOR.LONGITUDE          IS 'Longitude do foco (graus decimais).';
COMMENT ON COLUMN T_FOCO_CALOR.NUM_DIAS_SEM_CHUVA IS 'Numero de dias sem chuva no local ate a data do foco.';
COMMENT ON COLUMN T_FOCO_CALOR.PRECIPITACAO_MM    IS 'Precipitacao acumulada (mm) associada ao foco.';
COMMENT ON COLUMN T_FOCO_CALOR.RISCO_FOGO         IS 'Indice de risco de fogo calculado pelo INPE [0..1].';
COMMENT ON COLUMN T_FOCO_CALOR.FRP_MW             IS 'Fire Radiative Power (potencia radiativa do fogo) em megawatts.';

